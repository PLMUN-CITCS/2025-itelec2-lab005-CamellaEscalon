# Step 1: Define the variables a, b, and c
a = 10
b = 5
c = 3

# Step 2: Calculate a + b * c (without parentheses)
result1 = a + b * c
print("Result of a + b * c:", result1)

# Step 3: Calculate (a + b) * c (with parentheses)
result2 = (a + b) * c
print("Result of (a + b) * c:", result2)

# Step 4: Calculate a - b
result3 = a - b
print("Result of a - b:", result3)

# Step 5: Calculate a / b (standard division)
result4 = a / b
# Calculate a // b (floor division)
result5 = a // b
print("Result of a / b:", result4)
print("Result of a // b (floor division):", result5)

# Step 6: Calculate a % b (modulus)
result6 = a % b
# Calculate a ** c (exponentiation)
result7 = a ** c
print("Result of a % b (modulus):", result6)
print("Result of a ** c (exponentiation):", result7)

# Step 7: Calculate (a + b - c) * (a / b)
result8 = (a + b - c) * (a / b)
print("Result of (a + b - c) * (a / b):", result8)
